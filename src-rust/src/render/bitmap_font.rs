/// Width of a rendered glyph in pixels (may be scaled).
pub const GLYPH_WIDTH: u16 = 8;
pub const GLYPH_HEIGHT: u16 = 8;

/// Measure the pixel width of a string at the given scale.
pub fn text_width_scaled(text: &str, scale: u16) -> u16 {
    text.chars().count() as u16 * GLYPH_WIDTH * scale
}

/// Draw text using the built-in 8x8 bitmap font, scaled by `scale` (1, 2, 3, ...).
/// Each "on" bit becomes a `scale x scale` filled rectangle. No X core font needed.
pub fn draw_bitmap_text(
    conn: &x11rb::rust_connection::RustConnection,
    drawable: u32,
    gc: u32,
    x: i16,
    y: i16,
    text: &str,
    scale: u16,
) -> Result<(), anyhow::Error> {
    use x11rb::protocol::xproto::{ConnectionExt, Rectangle};

    let s = scale.max(1) as i16;
    let mut rects: Vec<Rectangle> = Vec::new();
    let mut cx = x;
    for ch in text.chars() {
        let code = ch as u32;
        if code >= 128 {
            // Unknown glyph: draw a filled box.
            for row in 0..8 {
                for col in 0..8 {
                    let on = (col + row) % 2 == 0;
                    if on {
                        rects.push(Rectangle {
                            x: cx + col as i16 * s,
                            y: y + row as i16 * s,
                            width: s as u16,
                            height: s as u16,
                        });
                    }
                }
            }
        } else {
            let glyph = &FONT8X8_BASIC[code as usize];
            for row in 0..8 {
                let rowbits = glyph[row];
                for col in 0..8 {
                    if rowbits & (1 << col) != 0 {
                        rects.push(Rectangle {
                            x: cx + col as i16 * s,
                            y: y + row as i16 * s,
                            width: s as u16,
                            height: s as u16,
                        });
                    }
                }
            }
        }
        cx += (GLYPH_WIDTH as i16) * s;
        if rects.len() > 256 {
            conn.poly_fill_rectangle(drawable, gc, &rects)?;
            rects.clear();
        }
    }
    if !rects.is_empty() {
        conn.poly_fill_rectangle(drawable, gc, &rects)?;
    }
    Ok(())
}
